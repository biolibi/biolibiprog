public class Planete{
    protected String nom;
    protected int nbPlutonium;
    protected int nbNeptunium;
    protected int nbTerbium;
    protected int nbganolinium;
    protected int nbThulium;


    public String getNom() {
        return nom;
    }


    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getNbPlutonium() {
        return nbPlutonium;
    }

    public void setNbPlutonium(int nbPlutonium) {
        this.nbPlutonium = nbPlutonium;
    }

    public int getNbNeptunium() {
        return nbNeptunium;
    }

    public void setNbNeptunium(int nbNeptunium) {
        this.nbNeptunium = nbNeptunium;
    }

    public int getNbTerbium() {
        return nbTerbium;
    }

    public void setNbTerbium(int nbTerbium) {
        this.nbTerbium = nbTerbium;
    }

    public int getNbganolinium() {
        return nbganolinium;
    }

    public void setNbganolinium(int nbganolinium) {
        this.nbganolinium = nbganolinium;
    }

    public int getNbThulium() {
        return nbThulium;
    }

    public void setNbThulium(int nbThulium) {
        this.nbThulium = nbThulium;
    }
}
